void merge_sort(int l, int r) {
    if (l == r) return;
    int m = l + (r - l) / 2;
    merge_sort(l, m);
    merge_sort(m + 1, r);

    int i = l, j = m + 1, t = 0;
    while (i <= m && j <= r)
        if (a[i] <= a[j])
            b[t++] = a[i++];
        else
            b[t++] = a[j++];
    while (i <= m) b[t++] = a[i++];
    while (j <= r) b[t++] = a[j++];
    for (int i = 0; i < t; i++) a[l + i] = b[i];
}

